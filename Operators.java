public class Operators {
	public static void main(String[] args) {
		System.out.println("Operators Class");
		
		// -2 unary operator
		// 2 + 3 binary operator
		// true ? "yes" : "no" ternary operator
		
		int x = 10 + 5;
		System.out.println(x);
		
		String y = "10" + "5";
		System.out.println(y);
		
		double z = 7 - 4 + 2 * 10;
		System.out.println(z);
		
		double w = (7 - 4 + 2) * 10;
		System.out.println(w);
	}	
	
}	