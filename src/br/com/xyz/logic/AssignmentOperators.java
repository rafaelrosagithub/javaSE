package br.com.xyz.logic;
public class AssignmentOperators {
	
	// += Addition
	// -= Subtraction
	// *= Multiplication
	// /= Division
	// %= Module
	
	public static void main(String[] args) {
		System.out.println("Assignement Operators");
		
		int x = 10;
		System.out.println(x += 2);
		System.out.println(x -= 2);
		System.out.println(x *= 2);
		System.out.println(x /= 2);
		System.out.println(x %= 2);
	}	
}	